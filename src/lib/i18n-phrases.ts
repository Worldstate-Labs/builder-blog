export function normalizeUiPhrase(value: string) {
  return value.replace(/\s+/gu, " ").trim();
}

export function translateUiPhrase(locale: UiLocale, phrase: string): string | null {
  if (locale === defaultUiLocale) return null;
  const normalizedPhrase = normalizeUiPhrase(phrase);
  if (!normalizedPhrase) return null;
  const exact =
    phraseTranslations[normalizedPhrase]?.[locale] ??
    coveragePhraseTranslations[normalizedPhrase]?.[locale] ??
    majorLocaleCoverageTranslations[normalizedPhrase]?.[locale];
  if (exact) return exact;
  for (const template of templateTranslations) {
    const match = normalizedPhrase.match(template.pattern);
    if (match) return template.render(locale, match);
  }
  return null;
}
